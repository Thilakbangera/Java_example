public class Loops {
    public static void main(String[] args){
        for(int i=0;i<=10;i++){
            //System.out.println(i);
        }
        //10-1
        for(int i=10;i>=1;i--){
            //System.out.println(i);
        }
        //While loop
        int i=1;
        while(i<=10){
            //System.out.println(i);
            i++;
        }
        while(i>=1){
            //System.out.println(i);
            i--;
        }
        //do while
        int k=1;
        do{
            System.out.println(k);
            k++;
        }while(k<=10);

    }
    
}
